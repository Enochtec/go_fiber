export function exportPurchases(purchases: Purchase[], shopName: string, exportedBy: string): string {
	const headers = [
		'Purchase No', 'Supplier', 'Ordered By', 'Date',
		'Items Count', 'Total Cost (KES)', 'Status', 'Notes'
	];
	const rows = purchases.map(p => [
		p.id.slice(0, 10).toUpperCase(),
		p.supplier_name ?? '',
		p.user_name ?? '',
		formatDate(p.created_at),
		String(p.items?.length ?? 0),
		formatCurrency(p.total),
		p.status,
		p.note ?? ''
	]);
	return metaHeader(shopName, exportedBy, 'Purchases') + buildCSV(headers, rows);
}

export interface InventoryRow {
	product_name: string;
	stock_qty: number;
	reorder_level: number;
	buying_price: number;
	selling_price: number;
	category_name?: string | null;
}

export function exportInventory(rows: InventoryRow[], shopName: string, exportedBy: string): string {
	const headers = [
		'Product', 'Category', 'Current Stock', 'Reorder Level',
		'Stock Status', 'Inventory Value (KES)', 'Retail Value (KES)'
	];
	const csvRows = rows.map(r => {
		const status = r.stock_qty <= 0 ? 'Out of Stock'
			: r.stock_qty <= r.reorder_level ? 'Low Stock'
			: 'In Stock';
		return [
			r.product_name,
			r.category_name ?? '',
			String(r.stock_qty),
			String(r.reorder_level),
			status,
			formatCurrency(r.buying_price * r.stock_qty),
			formatCurrency(r.selling_price * r.stock_qty)
		];
	});
	return metaHeader(shopName, exportedBy, 'Inventory') + buildCSV(headers, csvRows);
}
